package egovframework.example.sample.service;

public class CodeVO {
	//사용자의 데이터가 저장을 누를 때, 담기는 변수
	private int code;
	private int gid;
	private String name;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
